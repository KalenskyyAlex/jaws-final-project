package com.bezkoder.spring.dynamodb.model;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@DynamoDBTable(tableName = "tutorials")
@AllArgsConstructor
@NoArgsConstructor
@Data
public class TutorialDynamoDB {

	@DynamoDBHashKey
	@DynamoDBAutoGeneratedKey
	private String id;

	@DynamoDBAttribute
	private String title;

	@DynamoDBAttribute
	private String description;

	@DynamoDBAttribute
	private boolean published;

	@DynamoDBAttribute
	private boolean done;


	public TutorialDynamoDB(String title, String description, boolean published) {
		this.title = title;
		this.description = description;
		this.published = published;
		done = false;
	}

	@Override
	public String toString() {
		return "Tutorial [id=" + id + ", title=" + title + ", desc=" + description
				+ ", published=" + published + ", done=" + done + "]";
	}

}
